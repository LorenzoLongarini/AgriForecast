import os
import requests

def scarica_csv_da_link(lista_link, percorso_destinazione):
    """
    Scarica file CSV da una lista di URL e li salva in un percorso specificato.

    :param lista_link: Lista di link dei file CSV da scaricare.
    :param percorso_destinazione: Percorso della cartella dove salvare i file scaricati.
    """
    # Crea la cartella di destinazione se non esiste
    if not os.path.exists(percorso_destinazione):
        os.makedirs(percorso_destinazione)

    for i, link in enumerate(lista_link):
        try:
            print(f"Scaricando il file {i+1}/{len(lista_link)}: {link}")
            # Scarica il contenuto del file
            risposta = requests.get(link)
            risposta.raise_for_status()  # Controlla se la richiesta ha avuto successo

            # Estrai il nome del file dall'URL
            nome_file = link.split('/')[-1]

            # Percorso completo del file
            percorso_file = os.path.join(percorso_destinazione, nome_file)

            # Salva il file nel percorso specificato
            with open(percorso_file, 'wb') as file:
                file.write(risposta.content)

            print(f"File salvato con successo: {percorso_file}")
        except requests.exceptions.RequestException as e:
            print(f"Errore durante il download del file da {link}: {e}")
        except Exception as e:
            print(f"Errore sconosciuto con il file {link}: {e}")

